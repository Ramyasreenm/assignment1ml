#Calculate the area of a circle and assign the value to a variable name of _area_of_circle_
area_of_circle = 3.141592 * 30 * 30
print(area_of_circle)

#Circumference of a circle and assigning the value to a variable name of _circum_of_circle_
circum_of_circle = 2 * 3.141592 * 30
print(circum_of_circle)

#Taking the radius as user input and calculating the area.
rad = float(input("Enter radius: "))
print("Area of circle is:",3.141592 * rad * rad)